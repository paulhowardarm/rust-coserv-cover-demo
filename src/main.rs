#[tokio::main]
async fn main() {
    // Some hard-coded values
    // 1. The base URL of the Veraison CoSERV service
    let base_url = "https://veraison.test.linaro.org:11443";

    println!("Using CoSERV service at {}", base_url);
    
    let evidence = Evidence::decode(EAT_CCA_FVP_EVIDENCE.as_slice()).unwrap();

    // Bootstrap - call the discovery API
    let discovery = DiscoveryBuilder::new()
        .with_base_url(base_url.to_string())
        .build()
        .unwrap();
    let json_doc = discovery
        .get_coserv_discovery_document_json()
        .await
        .unwrap();

    // Extract the CoSERV request-response endpoint
    let rr_endpoint = json_doc.api_endpoints.get("CoSERVRequestResponse").unwrap();
    let rr_url = base_url.to_owned() + rr_endpoint;

    let query_runner = QueryRunnerBuilder::new()
        .with_request_response_url(rr_url)
        .build()
        .unwrap();

    // Make a CoSERV query for CCA reference values (based on the class ID)
    let v = ExtensionValue::Tag(600, Box::new(ExtensionValue::Bytes(evidence.platform_claims.impl_id.as_slice().into())));
    let id = ClassIdTypeChoice::Extension(v);
    let cca_fvp_class_map = ClassMapBuilder::new()
        .class_id(id)
        .build()
        .unwrap();

    let classes: Vec<StatefulClass> = vec![StatefulClassBuilder::new()
        .environment(cca_fvp_class_map)
        .build()
        .unwrap()];

    let rv_query = CoservQueryBuilder::new()
        .artifact_type(ArtifactTypeChoice::ReferenceValues)
        .result_type(ResultTypeChoice::CollectedArtifacts)
        .environment_selector(EnvironmentSelectorMap::Class(classes))
        .build()
        .unwrap();

    let rv_coserv = CoservBuilder::new()
        .profile(CoservProfile::Uri(
            "tag:arm.com,2023:cca_platform#1.0.0".into(),
        ))
        .query(rv_query)
        .build()
        .unwrap();

    // Make a CoSERV query for CCA trust anchors (based on the instance ID)
    let ueid = UeidType::new(evidence.platform_claims.inst_id.as_slice().into());
    let cca_fvp_instance_id = InstanceIdTypeChoice::Ueid(TaggedUeidType::new(ueid));
    let instances: Vec<StatefulInstance> = vec![StatefulInstanceBuilder::new()
        .environment(cca_fvp_instance_id)
        .build()
        .unwrap()];

    // create query map
    let ta_query = CoservQueryBuilder::new()
        .artifact_type(ArtifactTypeChoice::TrustAnchors)
        .result_type(ResultTypeChoice::CollectedArtifacts)
        .environment_selector(EnvironmentSelectorMap::Instance(instances))
        .build()
        .unwrap();

    // create coserv map
    let ta_coserv = CoservBuilder::new()
        .profile(CoservProfile::Uri(
            "tag:arm.com,2023:cca_platform#1.0.0".into(),
        ))
        .query(ta_query)
        .build()
        .unwrap();

    // Dump the queries to disk

    // Display the b64 strings
    println!("The reference value query is: {}", rv_coserv.to_b64_url().unwrap());
    println!("The trust anchor query is: {}", ta_coserv.to_b64_url().unwrap());

    // Run the two queries against the Veraison service
    let rv_result = query_runner.execute_query_unsigned(&rv_coserv).await.unwrap();
    let ta_result = query_runner.execute_query_unsigned(&ta_coserv).await.unwrap();

    let mut schemes = HashMap::new();
    let cca_scheme: Box<dyn Scheme> = Box::new(CcaScheme::new());
    schemes.insert("cca".to_string(), cca_scheme);

    let mut store = MemCoservStore::new();
    store
        .add_coserv_cbor_bytes(rv_result.to_cbor().unwrap().as_slice())
        .unwrap();
    store
        .add_coserv_cbor_bytes(ta_result.to_cbor().unwrap().as_slice())
        .unwrap();

    let verifier = Verifier::new(store, schemes);
    let result = verifier.verify("cca", &EAT_CCA_FVP_EVIDENCE, None).unwrap();

    println!("EAR: {}", serde_json::to_string(&result.ear).unwrap());
}
